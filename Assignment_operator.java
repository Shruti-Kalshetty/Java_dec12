
public class Assignment_operator {

	public static void main(String[] args) {
		 int a = 10;
	        int b = 5;

	        int c = a + b; 
	        System.out.println("c = " + c);

	        c += 5; 
	        System.out.println("c += 5: " + c);

	        c *= 2;
	        System.out.println("c *= 2: " + c);
	        
	        c/=5;
	        System.out.println("c/=5: " + c);

	        c %= 7; 
	        System.out.println("c %= 7: " + c);
	}

}
